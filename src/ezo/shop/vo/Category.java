package ezo.shop.vo;

public class Category {
	int no;
	String name;
	Integer mainCateNo;
	
	public Category() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getMainCateNo() {
		return mainCateNo;
	}

	public void setMainCateNo(Integer mainCateNo) {
		this.mainCateNo = mainCateNo;
	}
	
}
